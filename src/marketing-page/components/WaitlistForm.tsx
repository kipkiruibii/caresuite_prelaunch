export default function WaitlistForm() {
  const [formData, setFormData] = useState({
    name: '',
    email: '',
    country: '',
  });

  const handleChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    setFormData({
      ...formData,
      [e.target.name]: e.target.value,
    });
  };

  const handleSubmit = (e: React.FormEvent) => {
    e.preventDefault();
    console.log('Waitlist submission:', formData);
    // Here you would typically send the data to your backend
    alert('Thank you for joining the waitlist! We will contact you soon.');
    setFormData({ name: '', email: '', country: '' });
  };

  return (
    <Container maxWidth="lg" sx={{ py: 8 }} id="waitlist-form">
      <Box display="flex" justifyContent="center">
        <Box width={{ xs: '100%', md: '50%' }}>
          <StyledCard>
            <CardContent>
              <Typography variant="h4" component="h2" gutterBottom>
                Join Our Waitlist
              </Typography>
              <Typography variant="body1" paragraph>
                Be among the first to experience CareSuite. Provide your information below, and we'll keep you updated on our progress and invite you to participate in our pilot program. Your input will be greatly appreciated in helping us improve the product further and refine its features.
              </Typography>
              <Box component="form" onSubmit={handleSubmit} sx={{ mt: 4 }}>
                <TextField
                  fullWidth
                  label="Full Name"
                  name="name"
                  value={formData.name}
                  onChange={handleChange}
                  required
                  sx={{ mb: 2 }}
                />
                <TextField
                  fullWidth
                  label="Email"
                  name="email"
                  type="email"
                  value={formData.email}
                  onChange={handleChange}
                  required
                  sx={{ mb: 2 }}
                />
                <TextField
                  fullWidth
                  select
                  label="Business Location (Country)"
                  name="country"
                  value={formData.country}
                  onChange={handleChange}
                  required
                  sx={{ mb: 2 }}
                >
                  {countries.map((country) => (
                    <MenuItem key={country} value={country}>
                      {country}
                    </MenuItem>
                  ))}
                </TextField>
                <Button type="submit" variant="contained" size="large" fullWidth>
                  Join Waitlist
                </Button>
              </Box>
            </CardContent>
          </StyledCard>
        </Box>
      </Box>
    </Container>
  );
}